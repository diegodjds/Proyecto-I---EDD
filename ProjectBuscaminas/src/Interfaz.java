    private void cargarPartidaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cargarPartidaActionPerformed
        String save = fileChooser.abrirArchivo();
        String[] filas = save.split(";");
        
        Buscaminas buscaminas = new Buscaminas(filas.length, filas[0].split(",").length,0);
        buscaminas.setVisible(true);
        buscaminas.cargarPartida(save);
        this.dispose();
    }//GEN-LAST:event_cargarPartidaActionPerformed

    private void FilasStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_FilasStateChanged
        
        int Cantidad = (int)this.Filas.getValue();
        if(Cantidad < 3){
            this.Filas.setValue(3);
        }else if(Cantidad > 10){
            this.Filas.setValue(10);
        }
        if((int)Bombas.getValue() > (int)Columnas.getValue()*(int)Filas.getValue()){
            CheckBombas();
        }
    }//GEN-LAST:event_FilasStateChanged

    private void ColumnasStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_ColumnasStateChanged
        int Cantidad = (int)this.Columnas.getValue();
        if(Cantidad < 3){
            this.Columnas.setValue(3);
        }else if(Cantidad > 10){
            this.Columnas.setValue(10);
        }
        if((int)Bombas.getValue() > (int)Columnas.getValue()*(int)Filas.getValue()){
            CheckBombas();
        }
    }//GEN-LAST:event_ColumnasStateChanged
    
    private void CheckBombas(){
        int Cantidad = (int)this.Bombas.getValue();
        if(Cantidad < 1){
            this.Bombas.setValue(1);
        }else if(Cantidad > (int)Columnas.getValue()*(int)Filas.getValue()){
            this.Bombas.setValue((int)Columnas.getValue()*(int)Filas.getValue());
        }    
    }
    private void BombasStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_BombasStateChanged
        CheckBombas();
    }//GEN-LAST:event_BombasStateChanged

